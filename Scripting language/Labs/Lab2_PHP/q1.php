<!-- 1. Write PHP Script to demonstrate use of echo, print, print_r and var_dump functions.  -->

<?php
// Using echo
echo "Using echo: Hello, world!<br>";

// Using print
print "Using print: Hello, world!<br>";

// Using print_r with an array
$array = [1, 2, 3];
print_r($array);
echo "<br>";

// Using var_dump with a variable
$variable = "Hello, world!";
var_dump($variable);
?>
